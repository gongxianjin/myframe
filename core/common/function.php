// 递归 转义函数
function stripSlashesDeep($arr){
    foreach ($arr as $k => $v) {
        if(is_string($v)){
            $arr[$k] = stripslashes($v);
        }elseif(is_array($v)){
            $arr[$k] = stripSlashesDeep($v);
        }
    }
    return $arr;
}

// 检测敏感字符并删除
function removeMagicQuotes()
{
    if (get_magic_quotes_gpc()) {
        $_GET = isset($_GET) ? stripSlashesDeep($_GET ) : '';
        $_POST = isset($_POST) ? stripSlashesDeep($_POST ) : '';
        $_COOKIE = isset($_COOKIE) ?  stripSlashesDeep($_COOKIE) : '';
        $_SESSION = isset($_SESSION) ?   stripSlashesDeep($_SESSION) : '';
    }
}

// 检测自定义全局变量（register globals）并移除
function unregisterGlobals()
{
    if (ini_get('register_globals')) {
        $array = array('_SESSION', '_POST', '_GET', '_COOKIE', '_REQUEST', '_SERVER', '_ENV', '_FILES');
        foreach ($array as $value) {
            foreach ($GLOBALS[$value] as $key => $var) {
                if ($var === $GLOBALS[$key]) {
                    unset($GLOBALS[$key]);
                }
            }
        }
    }
}

//php获取当前时间的毫秒数的方法
function getMillisecond() {
    list($s1, $s2) = explode(' ', microtime());
    return (float)sprintf('%.0f', (floatval($s1) + floatval($s2)) * 1000);
}


